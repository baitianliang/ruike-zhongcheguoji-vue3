class I18nManager {
    constructor() {
        this.currentLang = this.detectLanguage();
        this.observers = new Set(); // 用于存储需要更新的组件
    }

    detectLanguage() {
        const savedLang = localStorage.getItem('app_language');
        if (savedLang) {
            return savedLang;
        }
        const browserLang = navigator.language || navigator.userLanguage;
        return browserLang.startsWith('zh') ? 'zh' : 'en';
    }

    setLanguage(lang) {
        if (!i18nData[lang]) return;
        
        this.currentLang = lang;
        localStorage.setItem('app_language', lang);
        
        // 通知所有观察者更新
        this.observers.forEach(update => update());
        
        // 触发全局事件
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    }

    getText(key) {
        if (!this.currentLang) {
            this.currentLang = this.detectLanguage();
        }
        return i18nData[this.currentLang]?.[key] || key;
    }

    formatDate(date) {
        if (!date) return '';
        const d = new Date(date);
        return new Intl.DateTimeFormat(this.currentLang === 'zh' ? 'zh-CN' : 'en-US', {
            year: 'numeric',
            month: '2-digit',
            day: '2-digit',
            hour: '2-digit',
            minute: '2-digit',
            hour12: false
        }).format(d);
    }

    // 注册观察者
    registerObserver(updateCallback) {
        this.observers.add(updateCallback);
        return () => this.observers.delete(updateCallback);
    }

    // 创建语言切换器
    createLanguageSwitcher() {
        return {
            template: `
                <select 
                    v-model="currentLang" 
                    @change="changeLanguage"
                    class="lang-switcher"
                >
                    <option value="zh">中文</option>
                    <option value="en">English</option>
                </select>
            `,
            data() {
                return {
                    currentLang: window.i18nManager.currentLang
                };
            },
            methods: {
                changeLanguage() {
                    window.i18nManager.setLanguage(this.currentLang);
                }
            }
        };
    }
}

// 创建全局实例
const i18nManager = new I18nManager();
window.i18nManager = i18nManager;

// Vue 自定义指令
const i18nDirective = {
    mounted(el, binding) {
        updateText(el, binding);
        
        // 注册观察者，当语言改变时更新
        const unsubscribe = i18nManager.registerObserver(() => {
            updateText(el, binding);
        });
        
        // 在元素上存储清理函数
        el._i18nCleanup = unsubscribe;
    },
    updated(el, binding) {
        updateText(el, binding);
    },
    beforeUnmount(el) {
        // 清理观察者
        if (el._i18nCleanup) {
            el._i18nCleanup();
        }
    }
};

// 辅助函数：更新元素文本
function updateText(el, binding) {
    const { value } = binding;
    
    if (typeof value === 'string') {
        // 简单键值
        const text = i18nManager.getText(value);
        applyTranslation(el, text);
    } else if (typeof value === 'object') {
        // 复杂指令，可以处理多种属性
        const { key, attr } = value;
        if (key) {
            const text = i18nManager.getText(key);
            if (attr === 'placeholder' && el.tagName === 'INPUT') {
                el.placeholder = text;
            } else if (attr === 'title') {
                el.title = text;
            } else {
                applyTranslation(el, text);
            }
        }
    }
}

// 应用翻译到元素
function applyTranslation(el, text) {
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.setAttribute('placeholder', text);
    } else if (el.hasAttribute('title')) {
        el.setAttribute('title', text);
    } else {
        // 处理包含子元素的元素
        const hasElementChildren = Array.from(el.childNodes).some(node => node.nodeType === 1);
        
        if (hasElementChildren) {
            // 查找第一个文本节点
            const textNode = Array.from(el.childNodes).find(
                node => node.nodeType === 3 && node.nodeValue.trim() !== ''
            );
            
            if (textNode) {
                textNode.nodeValue = text;
            } else {
                // 如果有元素子节点但没有文本节点，添加文本
                const children = Array.from(el.children);
                el.innerHTML = '';
                children.forEach(child => el.appendChild(child));
                if (children.length > 0) {
                    el.appendChild(document.createTextNode(' ' + text));
                } else {
                    el.textContent = text;
                }
            }
        } else {
            el.textContent = text;
        }
    }
}

// 组合式 API 工具函数
function useI18n() {
    const getText = (key) => i18nManager.getText(key);
    const formatDate = (date) => i18nManager.formatDate(date);
    const setLanguage = (lang) => i18nManager.setLanguage(lang);
    const currentLang = () => i18nManager.currentLang;
    
    return {
        getText,
        formatDate,
        setLanguage,
        currentLang,
        t: getText, // 别名，更简洁
        lang: currentLang
    };
}

// Vue 插件
const i18nPlugin = {
    install(app) {
        // 注册自定义指令
        app.directive('i18n', i18nDirective);
        
        // 提供全局实例
        app.config.globalProperties.$i18n = i18nManager;
        
        // 提供 i18nManager 以便在 setup 中使用
        app.provide('i18nManager', i18nManager);
        
        // 添加全局方法
        app.mixin({
            methods: {
                $t(key) {
                    return i18nManager.getText(key);
                }
            }
        });
    }
};

// 导出主要功能
export { i18nDirective, i18nManager, i18nPlugin, useI18n };